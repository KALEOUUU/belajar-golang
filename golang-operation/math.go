package main

import "fmt"

func main() {
	a := 10
	b := 3

	// penjumlahan
	sum := a + b
	fmt.Println("Penjumlahan:", sum)

	// pengurangan
	diff := a - b
	fmt.Println("Pengurangan:", diff)

	// perkalian
	product := a * b
	fmt.Println("Perkalian:", product)

	// pembagian
	quotient := a / b
	fmt.Println("Pembagian:", quotient)

	// sisa bagi
	remainder := a % b
	fmt.Println("Sisa Bagi:", remainder)
}